// Create AveragePCM Class to find the average percentage marks in PCM
public class AveragePCM{
   public static void main(String args[]){ 

      // declare and initialize marks for each subject
      int maths = 94;
      int physics = 95;
      int chemistry = 96;
      int total = maths + physics + chemistry;   
      double avg = total/3.0;
       
      // Display the result on the screen
      System.out.println("Sam's average marks in PCM is "+avg);   
   }
} 






