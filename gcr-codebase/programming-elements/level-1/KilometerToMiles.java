// Create KilometerToMiles Class to covert distance from kilometer to miles
public class KilometerToMiles{
   public static void main(String args[]){ 

      // declare and initialize the distance in kilometers
      double kilometer = 10.8;   
      double miles = kilometer * 1.6;
       
      // Display the result on the screen
      System.out.println("The distance "+kilometer+" km in miles is "+miles);   
   }
} 






