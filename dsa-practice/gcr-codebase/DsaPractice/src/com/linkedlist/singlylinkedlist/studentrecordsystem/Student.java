package com.linkedlist.singlylinkedlist.studentrecordsystem;

public class Student {
    int rollNo;
    String name;
    int age;
    char grade;

    public Student(int rollNo, String name, int age, char grade) {
        this.rollNo = rollNo;
        this.name = name;
        this.age = age;
        this.grade = grade;
    }
}
